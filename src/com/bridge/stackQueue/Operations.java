public class Operations {

    public static void main(String[] args) {
        Stack stack = new Stack();

        stack.push();
        stack.showElements();
        stack.peek();
        stack.pop();
        stack.showElements();
        stack.peek();
        stack.pop();
        stack.showElements();
        stack.peek();
        stack.pop();
        stack.showElements();
    }
}
